//parameters are the variables that we pass to a function when we call it.
//arguments are the values that we pass to a function when we call it.
//In JavaScript, parameters are the names listed in the function definition, while arguments are the real values passed to the function when it is invoked.
function add(a, b) { // a and b are parameters
    return a + b;
}
console.log(add(2, 3)); // 2 and 3 are arguments